<h2 class="dipay-3 text-center">Programa  la Limba și Literatura Română 2019-2020</h2><hr>
<h3 class="dipay-4 font-italic">Dacă vizualizați acest conținut pe telefonul mobil, recomandăm folosirea în modul "landscape"</h3><hr>
<p>După cum știți, unele modificări au avut loc în programul de licență în 2020, astfel încât programul semestrului doi nu va mai fi inclus. În ceea ce privește disciplina limbii române, cele mai importante schimbări sunt legate de excluderea conținuturilor legate de perioada postbelică (romane postbelice, poezie postbelică, dramă postbelică; gen cultural / literar: postmodernism). În același timp, următorii autori sunt, de asemenea, excluși din lista autorilor standard: Marin Preda, Nichita Stănescu și Marin Sorescu. Prin urmare, nu va mai trebui să le studiați munca.</p><br>

<p>Vă recomandăm să vizitați site-ul web al Ministerului Educației și Cercetării, dacă doriți să consultați în detaliu Programa la Limba si Literatura Română pentru anul 2020.</p><br><br>


