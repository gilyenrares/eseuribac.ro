		<p>REGIMUL FANARIOT</p>

		<p>Începând din secolul al XVII-lea Imperiul Otoman trece printr-o perioadă decisivă cunoscută sub numele de ,, problema orientală '' . Imperiul Habsburgic și cel Țarist vor încercă să profite de această criză și să înlocuiască dominația otomana în zona balcanilor.</p>

		<p>Noul context internațional i-a determiant pe unii domnitori să încheie alianțe fie cu Imperiul Țarist ( cazul lui Dimitrie Cantemir), fie cu Imperiul Habsburgic ( cazul lui Șerban Cantacuzino și al lui Constantin Brâncoveanu.</p>

		<p>În 1711, în Moldova și în 1716 îs Țara Românească se instaurează regimul fanariot. Acest regim a reprezentat expresia dominației politice și economice a Imperiului Otoman pe fondul eccentuarii problemei orientale.  Domnitorul fiind numit de sultan, dar era numit contra unei sume imense de bani. Cu această ocazie funcția de domnitor era scurtă pentru că mereu se găsea cinva dispus să ofere mai mult.</p>

		<p>Domnitorul nu putea duce o politică externă proprie dar a fost folosit deseori de turci că agenți diplomatici în relație cu marile puterii pentru proprile interese.</p>
		<p>Țara Românească își păstrează autonomia, instituțiile, legislația, doar oastea este desfintata și înlocuită cu drupe de arnăuți.</p>
		  <p>Regimul fanariot a fost considerat de Nicolae Iorga o perioadă de despotism luminat. Principalele reforme realizate au fost:</p>
		  <p>Dezvoltarea învățământului;</p>
		  <p>Impozit fix, plătit în patru rate;</p>
		  <p>Desfintarea legării de glie în 1746 în Țara Românească și 1749 în Moldova;</p>
		  <p>Redactarea unor proiecte de constituție ce preconizau instaurarea republicii și organiarea de alegeri libere;</p>
		  <p>Apariția unor coduri de legi;</p>
