		<h2 class="dipay-3 text-center">Evul Mediu</h2><hr>
		<p>  Principalele instituții ale Evului Mediu au fost Adunarea țării, domnia, sfatul domnesc,Biserica.</p>

		<p>Domnia  (  dominus  )  :   instituție  centrală,  domnul  este  stăpânul  țarii,  șef  alarmatei ( voievod), judecător suprem, bate monedă, fixează impozite , emite actede proprietate, acordă privilegii și imunități, hotareste politica externă, numește și revocă dregătorii, numește mitropolitul și episcopii, ctitorește lacașe de cult, are drept de grațiere. </p>
		<p>Este ales de adunarea țării, încoronat de mitropolit și isitransmite tronul pe cale electivă ereditară ( unul dintre toții urmașii lui fiindales).
		Domnitorul este considerat de origine divină ( mitropolitul poate tinelocul acestuia în situații de urgență ).</p>

		<p>Bsierica in Evul Mediu</p>

  		<p>De-a lungul Evului Mediu, Biserica ortodoxă, a avut un important rol economic, politic, cultural și social.</p>
   		<p>Principalele momente istorice :</p>
   
      	<p>1054- Marea Schismă</p>
      
      	<p>1359- infintarea mitropoliei de la Curtea de Argeș, ce se vă muta în 1665 la București;</p>
      
      	<p>1401- Mitropolia de la Suceava de către Alexandru cel Bun</p>
      
      	<p>În dobrogea a existat o mitropolie la Vicina încă din 1285.</p>
      
      	<p>În transilvaia, după expresia lui Lucian Blaga, religia a fost ,, contrajortul spiritual '' al conștiinței de neam.</p>
      
      	<p>Intre Biserică și domnie, relațiile au fost de colaborare și independența. Mitropolitul îl unge pe domn, este membru în sfatul domnesc, primul sfetnic al domnului. Conduce sălile diplomatine, ține locul domnului în caz de vacanță a tronului, judecă abaterile clerului.</p>
      
		<p>Domnitorii români s-au manifestat că protectorii ai ortodoxismului, nu doar în spațiul românesc, ci în întreagă lume ortodoxă. </p>

		<p>Au ridicat lăcășuri de cult, au făcut dări către muntele Athos și au susținut financiar 46 de școli.</p>

