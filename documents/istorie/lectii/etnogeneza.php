		<h2 class="dipay-3 text-center">Etnogeneza românească între ştiinţă şi politică <br>(teoria imigraţionista , rosleriana )</h2><hr>
		<p>Susţinători: I Szamoszkozy, I Sulze, Chr. Angel, R. Roesler</p>
		</p>Adversari: Miron Costin, Grigore Ureche, Ion Neculce</p>
		</p>Corifei: B.P Haşdeu, A.B Xenopol, Nicolae Iorga, Eduard Gibbon</p>
		</p>Scopul teoriei imigraţioniste: să justifice dominaţia străină asupra Transilvaniei şi lipsa de drepturi politice a românilor</p>
		</h3>ARGUMENT</h3>
		</p>1) Ocupaţia romană nu a fost în măsură să declare romanizarea, timpul fiind prea scurt:</p>
		</p>2) Dacii au fost exterminate în urma celor două războaie daco-romane;</p>
		</p>3) Retragerea aureliana a însemnat, părăsirea complete a provinciei Dacia;</p>
		</p>4) Asemănările între limba romană şi limba albaneză.</p>
		</p>5) Nu există în limba romana cvinte provenind de la vechii romani;</p>
		</p>6) Nu există izvoare istorice referitoare la romani şi ţara lor până în secolul al XII-lea;</p>
		</h3>CONTRAARGUMENTE</h3>
		</p>1) Cazul Galiei şi Hispaniei, unde romanizarea s-a realizat într-un interval de timp comparabil;</p>
		</p>2) Latinitatea limbii române;</p>
		</p>3) Continuitatea dacică;</p>
		</p>4) Continuitatea daco-romana;</p>
		</p>5) Existenţa numelor proprii;</p>
		</p>6) Romanii sunt amintiţi în izvoarele istorice, începând încă din secolul VII, acestea provenind de la muntele Athos sau reprezentând lucrări redactate de istorici arabi, bizantini sau turci.</p>
		</p>Romanitatea romanilor a susţinut revendicările romanilor din Transilvania exprimată pentru prima dată în Supplex Libelux elaborate în 1791.</p>
